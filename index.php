            <h3>Introduction</h3>
            &nbsp&nbsp&nbsp&nbspMany students do not clearly envision their professional future and are not really able to foresee the economic, technological and, most of all, managerial changes affecting their future profession. We certainly must rely upon our students to finally find their own way, but we must also give them a sense of responsibility so as to enable them to take care of their career, as soon as possible and at best, in accordance with their genuine wishes. Many freshmen inherited a quest for perfection and fear the little-known employment market. It is therefore advisable to help students evolve from exemplary learners to fulfilled professionals in promoting their active participation in their own choices.
            Personality tests first allow to “demystify” the recruitment process often implemented by recruiters and head hunters. Personality type tests are also useful tools for helping engineering colleges to understand their students and reform instruction that can benefit students of various types. Personality tests and subsequent counselling will help students to develop an introspection strategy which will facilitate their choices in accordance with their education, culture, values, family, and friend contexts.<b> For this purpose, Personality Profiling is introduced at SRKR Engineering College, with a view to identifying or discussing the strengths and potentials of its students.</b>
            <h3>Learning Outcomes</h3>
            Personality Profiling will help a student to<br>
            <ol>
                <li>know oneself and identify one’s set of skills in order to better define one’s career choices</li>
                <li>understand the areas of improvement and design a road map for personal and professional development</li>
                <li>develop his personality holistically</li>
